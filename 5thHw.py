print('Divide of a={} and b={} is equal to c={}'.format(a, b, a / b))
print('*'*10)

print('Int division of a={} and b={} is equal to c={}'.format(a, b, a // b))
print('*'*10)

print('Remainder of a={} and b={} is equal to c={}'.format(a, b, a % b))
print('*'*10)

